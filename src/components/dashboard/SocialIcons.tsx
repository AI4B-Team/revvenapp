const normalizePlatformId = (platformId: string) => {
  const id = (platformId || '').toLowerCase();
  if (id === 'twitter') return 'x';
  return id;
};

export const getPlatformIcon = (
  platformId: string,
  className?: string,
  variant: PlatformIconVariant = 'brand'
) => {
  const normalized = normalizePlatformId(platformId);
  const platform = socialPlatforms.find((p) => p.id === normalized);
  if (!platform) return null;

  const IconComponent = platform.Icon;
  return <IconComponent className={className} variant={variant} />;
};
